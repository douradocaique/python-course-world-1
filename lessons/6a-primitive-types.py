# Tipos primitivos em Python

# int   -> números inteiros
x = 7

# float -> números decimais (ponto flutuante)
y = 7.3

# bool  -> valores lógicos (booleanos) - sempre com inicial maiúscula
verdadeiro = True
falso = False

# str   -> cadeias de caracteres (strings)
mensagem = "olá, mundo!"
